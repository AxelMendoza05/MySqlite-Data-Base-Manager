    public void dll(){
        String nombre = txtName.getText();
        String When = cbWhen.getSelectedItem().toString();
        String Action = cbAction.getSelectedItem().toString();
        String table = cbTable.getSelectedItem().toString();
        
        
        data="CREATE TRIGGER "+nombre+" ";
        String dll ="CREATE TRIGGER "+nombre+"\n";
        int predecir=1;
        
        if(!When.equals("NULL")){
            data =data+" "+When+" "+ Action+" On "+ table;
            dll = dll +" "+When+" "+ Action+" On "+ table;
            if(p==true){
                data = data+" WHEN "+ jTrigger1.getText() +" BEGIN " + jTrigger.getText() +";";
                dll = dll + "\n WHEN \n"+ jTrigger1.getText() +"\n BEGIN \n" + jTrigger.getText() +";";
            }else{
                data = data + "\n BEGIN " + jTrigger.getText()+";";
                dll = dll + "\n BEGIN \n" + jTrigger.getText()+";";
            }
        }else{
            data =data+" "+ Action+" On "+ table;
            dll = dll +" "+ Action+" On "+ table;
            if(p==true){
                data = data+" WHEN "+ jTrigger1.getText() +" BEGIN " + jTrigger.getText() +";";
                dll = dll + "\n WHEN \n"+ jTrigger1.getText() +"\n BEGIN \n" + jTrigger.getText() +";";
            }else{
                data = data + "\n BEGIN " + jTrigger.getText()+";";
                dll = dll + "\n BEGIN \n" + jTrigger.getText()+";";
            }
        }
        
        data = data + "END;";
        dll = dll + "\nEND;";
        jDLL.setText(dll);
  }
  public void llenarTablas(){
        ResultSet result = null;
        try{
            
            PreparedStatement st = connectBD.prepareStatement("SELECT * FROM sqlite_master WHERE type = \"table\"");
            result = st.executeQuery();
            while(result.next()){
                cbTable.addItem(result.getString(2));
            }  
        }catch(Exception e){
             JOptionPane.showMessageDialog(null, e.getMessage().toString());
        }
  }
    
