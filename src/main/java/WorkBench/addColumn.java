    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!txtName.getText().equals("")){
            
            String type = cmbType.getSelectedItem().toString();
            String data=txtName.getText() +" "+ type;
            
            Table.name = txtName.getText();
            Table.type = type;
        
            if(cbPrimary.isSelected() && cbAutoincrement.isSelected()){
                data =data+" PRIMARY KEY AUTOINCREMENT ";
            }else if(cbPrimary.isSelected()){
                data =data + " PRIMARY KEY ";
                Table.p1=1;
            }else{
                Table.p1=0;
            }

            if(cbFor.isSelected()){
                data = data+ " REFERENCES "+ table +"("+column+")";
                Table.reference=1; 
            }else{
                Table.reference=0; 
            }

            if(cbUni.isSelected()){
                data = data+" UNIQUE ";
                Table.uni=1;
            }else{
                Table.uni=0;
            }

            if(cbNull.isSelected()){
                data=data+" NOT NULL ";
                Table.not=1;
            }else{
                Table.not=0;
            }
        
        Table.consul = data;
        
        this.dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


